numero1 = float(input("Digite o primeiro numero: "))
numero2 = float(input("Digite o segundo numero: "))
# Divide o segundo numero pelo primeiro e o resultado é
resultado = round(numero2 / numero1, 2)

# resultado da divisão com duas casas decimais
print(f"O resultado da divisão de {numero2} por {numero1} com duas casas decimais é {resultado}.")